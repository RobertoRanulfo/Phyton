import math
num = int(input('Digite um número '))
raiz = math.sqrt(num)
print('A raiz de {} é {}'.format(num, math.ceil(raiz)))
# Escrevendo import, você importa a biblioteca de recursos matemáticos "match"
# sqrt é uma fórmula para raiz quadrada e ceil é para arredondar um número para cima sendo ele inteiro

