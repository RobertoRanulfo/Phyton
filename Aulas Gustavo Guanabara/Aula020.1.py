def mensagem(x):   #criamos uma função chamada mensagem dentro dela terá uma variável x que vai receber
    print('-=' * 15) #o que for digitado dentro do parêntese
    print(x)
    print('-=' * 15)


mensagem('SISTEMA DE ALUNOS')


def título(t):          #criou-se a função título com um parâmetro recebendo a variável t
    print('-' * 30)     #abaixo ele vai dizer o que vai fazer com o t
    print(t)
    print('-' * 30)


