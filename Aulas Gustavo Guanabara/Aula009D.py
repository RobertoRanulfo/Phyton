frase = ('Curso em vídeo Python')
print(frase.replace('Python', 'Android'))
# Com o replace podemos tirar algo e colocar outra coisa no lugar
print('Curso' in frase)
print(frase.find('Python'))
# o 'in' verifica se existe o que foi citado respondendo com true or false, equanto a fórmula 'find'
# verifica se existe e diz em qual caracterte está localizado.
# Caso o 'find' não encontre o solicitado ele retorna com -1