num = [2, 5, 9, 1]
print(num)
num[2] = 3 # estou dizendo que agora o dado número 2 (que vale 9) agora vai ser igua a 3
print(num)
num[3] = 10
print(num)
num.append(22) #função append acrescenta um dado após o último dado
print(num)
num.sort() #função sort deixa os dados da lista em ordem
print(num)
num.sort(reverse=True) #função deixa os dados da lista em ordem descrescente
print(num)