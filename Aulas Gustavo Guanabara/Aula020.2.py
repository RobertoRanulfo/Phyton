def soma(a,b): #definimos uma função como soma (poderia ser qualquer outro nome) com as variáveis a e b
    s = a + b  #e criamos uma variável s para receber a soma e posteriormente printamos o resultado da soma
    print(s)


soma(2, 3) #basta digitar as variáveis dentro do parêntese elas entram na variável na mesma ordem inserida
           #no parêntese. o a vai receber 2 e o b vai receber 3, se no parêntese tiver outro número dará erro


