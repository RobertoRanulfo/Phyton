galera = [['João', 19], ['Ana', 33], ['Joaquin', 13], ['Maria', 45]]
print(galera[0]) # mostra o elemento 1 da tupla que é ['João', 19]
print(galera[0][1]) # nesse caso ele puxa o elemento 0 que é joão, 19, mas apenas o termo 1 que é 19
print(galera[2][1]) # nesse caso ele puxa o elemento 2 que é ['Joaquin', 13], apenas o termo 1 que é 13