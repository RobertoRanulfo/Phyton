frase = ('Curso em Vídeo Python')
print(frase.split())
# A função split desseca a variável por palavras
divido = frase.split()
print(divido)
# Nesse formato criamos uma nova variável com o resultado chamado divido
# No caso agora cada palavra seria um dado e as numerações vão por palavras e não em cada letra
print(divido[0])
# Como os dados estão em palavras quando chamamos o dado zero escreve a primeira palavra que seria o primeiro dado
print(divido[0][1])
# Posso também pedir a letra dentro do dado 0

